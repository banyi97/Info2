    {
        //
        $query = DB::table('users')
            ->select('users.id', 'users.email', 'users.name', 'users.created_at')      
            ->get();  
        $users = array();
        foreach($query as $user){
            $data = array();
            $data['id'] = $user->id;
            $data['name'] = $user->name;
            $data['email'] = $user->email;
            $data['created_at'] = $user->created_at;
            $users['user'][] = $data;
        }
        return view('admin')->with([
            'users'=> json_encode( $users )
        ]);
    }

